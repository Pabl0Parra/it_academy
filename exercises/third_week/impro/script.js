function validateParameters(){
  const firstUserParameter = prompt(
    "What would you like to input as the first parameter?",
    "array, function, object, varaible..."
  );

  if (firstUserParameter !== "array") {
    console.log("The first parameter is not an array");
  }

  const secondUserParameter = prompt(
    "What would you like to input as the second parameter?",
    "array, function, object, variable..."
  );

  if (secondUserParameter !== "function") {
    console.log("The second parameter is not a function");
  }
}







